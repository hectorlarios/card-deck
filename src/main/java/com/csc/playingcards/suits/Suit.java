package com.csc.playingcards.suits;

import com.csc.playingcards.cards.Card;
import com.csc.playingcards.cards.Rank;

public class Suit
{
  //****************************************************************************************************
  //Begin - Constructor
  //****************************************************************************************************	
  public Suit(SuitType type, Rank[] rankList)
  {
    setType(type);

    setRanks(rankList);
  }
  //****************************************************************************************************
  //End  - Constructor
  //****************************************************************************************************
  //****************************************************************************************************
  //Begin  - Get/Set Methods
  //****************************************************************************************************
  private Rank[] ranks;
  public Rank[] getRanks(){return ranks;}
  public void setRanks(Rank[] value)
  {
    ranks = value;

    createCards();
  }

  private Card[] cards;
  public Card[] getCards(){return cards;}
  //****************************************************************************************************
  //End - Get/Set Methods
  //****************************************************************************************************	
  //****************************************************************************************************
  //Begin  - Private Methods
  //****************************************************************************************************
  private void createCards()
  {
    int i;

    int l = ranks.length;

    cards = new Card[l];

    Card card;

    Rank rank;

    char suiteSymbol = getSuitSymbol();

    for (i = 0; i < l; i++)
    {
      rank = ranks[i];

      card = new Card(rank,suiteSymbol);

      cards[i] = card;
    }
  }

  private char getSuitSymbol()
  {
    char value = '♠';

    switch (suitType)
    {
      case Hearts:
        value = '♥';
        break;
      case Diamonds:
        value = '♦';
        break;
      case Clubs:
        value = '♣';
        break;
    }

    return value;
  }
  //****************************************************************************************************
  //End - Private Methods
  //****************************************************************************************************
  //****************************************************************************************************
  //Begin  - Protected Methods
  //****************************************************************************************************
  protected void setType(SuitType value)
  {
    suitType = value;

    name = value.name();
  }
  //****************************************************************************************************
  //End - Protected Methods
  //****************************************************************************************************
  //****************************************************************************************************
  //Begin  - Public Methods
  //****************************************************************************************************
  private String name = "";
  public String getName() {return name;}

  private SuitType suitType;
  public SuitType getSuitType(){return suitType;}
  //****************************************************************************************************
  //End - Public Methods
  //****************************************************************************************************			
}