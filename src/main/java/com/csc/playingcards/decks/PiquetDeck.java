package com.csc.playingcards.decks;

import com.csc.playingcards.cards.Rank;

public class PiquetDeck extends StandardDeck
{
  //****************************************************************************************************
  //Begin - Constructor
  //****************************************************************************************************
  public PiquetDeck()
  {
    name = PIQUET;
  }
  //****************************************************************************************************
  //End  - Constructor
  //****************************************************************************************************
  //****************************************************************************************************
  //Begin  - Protected Methods
  //****************************************************************************************************
  @Override
  protected void createCardRanks()
  {
    cardRanks = new Rank[]
      {
        Rank.ACE,
        Rank.KING,
        Rank.QUEEN,
        Rank.JACK,
        Rank.TEN,
        Rank.NINE,
        Rank.EIGHT,
        Rank.SEVEN
      };
  }
  //****************************************************************************************************
  //End - Protected Methods
  //****************************************************************************************************
}