package com.csc.playingcards.cards;

public enum Rank
{
  //-------------------------
  //enums
  //-------------------------
  ACE("A"),
  KING("K"),
  QUEEN("Q"),
  JACK("J"),
  TEN("10"),
  NINE("9"),
  EIGHT("8"),
  SEVEN("7"),
  SIX("6"),
  FIVE("5"),
  FOUR("4"),
  THREE("3"),
  DEUCE("2"),
  BIG_JOKER("BJ"),
  LITTLE_JOKER("LJ");
  //-------------------------
  //strings
  //-------------------------
  private String value;
  //****************************************************************************************************
  //Begin - Constructor
  //****************************************************************************************************	
  private Rank(String label)
  {
    value = label;
  }
  //****************************************************************************************************
  //End  - Constructor
  //****************************************************************************************************
  //****************************************************************************************************
  //Begin  - Public Methods
  //****************************************************************************************************
  public String getValue(){return value;}
  //****************************************************************************************************
  //End - Public Methods
  //****************************************************************************************************	
}